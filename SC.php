<p>
  <b>Pluriforme samenleving</b><br>
We leven nou eenmaal in een pluriforme samenleving, een samenleving die er op lange termijn veel baat bij heeft als er word geintegreerd. Mensen moeten niet worden buitengesloten en aan hun lot worden overgelaten. <br>
Daarom vinden wij dat iedereen moet integreren en in ieder geval de taal op een niveau beheersen dat ze zichzelf kunnen redden in Nederland. 
Daarbij zou dan vooral een taak zijn weggelegd voor andere allochtonen die de taal van de vluchteling en het Nederlands beheersen, om hen op weg te helpen. 
</p>
