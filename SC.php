<p>
  <b>Discriminatie</b><br>
De laatste tijd is discriminatie een hot topic in de media. Het is alweer bijna Sinterklaas en het lijkt een traditie te worden dat dan ook de zwartepieten discussie weer in volle gang word gezet.
Dan hebben we ook nog het proces tegen Geert Wilders over zijn uitspraak "minder marokkanen" en of dat nou discriminatie is of niet.<br>
Wij vinden dat er moet worden onderzocht of er sprake is van discriminatie, waar dat zich afspeelt en waardoor het komt voordat er overhaaste beslissingen worden genomen.
Ook moet er rekening gehouden worden met neutrale burgers bij demonstraties, het is bijvoorbeeld belachelijk dat kinderen de dupe zijn van de zwartepieten discussie bij de intocht van Sinterklaas. 
</p>
