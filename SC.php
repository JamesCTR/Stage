  <b>Vluchtelingenproblematiek</b><br>
Wij vinden dat alle vluchtelingen de moeite moeten nemen om in te burgeren. Ook moet er strikt worden toegezien dat het echt vluchtelingen zijn en geen gelukszoekers. Verder moeten er bij de Europese grenzen goed worden gecontroleerd op wapens en of explosieven. De hoeveelheid vluchtelingen die een land opneemt zou door middel van verschillende factoren worden vastgesteld zoals bijvoorbeeld: inwoner aantal, grootte van het land, en beschikbare hoeveelheid woonruimte.
</p>
